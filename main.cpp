#include <iostream>
using namespace std;

int main()
{
	int Time;
	Time = 10;

	cout << Time + 1 << endl;
	cout << Time - 1 << endl;
	cout << Time * 2 << endl;
	cout << Time / 2 << endl;
	cout << Time % 3 << endl;

	float Second;
	Second = 1.9f;

	cout << Second + 0.1 << endl;
	cout << Second - 0.9 << endl;
	cout << Second * 10 << endl;
	cout << Second / 1.9 << endl;

}